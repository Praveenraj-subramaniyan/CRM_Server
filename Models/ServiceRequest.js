const mongoose = require("mongoose");

const serviceRequestSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    company: {
      type: String,
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    status: {
      type: String,
      default: "Created",
    },
  },
  { collection: "ServiceRequest" }
);

module.exports = mongoose.model("ServiceRequest", serviceRequestSchema);